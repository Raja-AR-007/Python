#                            Number System
#        decimal to binary
print(bin(24)) #0b11000

#        decimal to octal
print(oct(24)) #0o30

#        decimal to hexadecimal
print(hex(24)) #0x18


#        binary to decimal
print(0b11001) #25

#        binary to octal
print(0o110110) #36936

#        binary to hexadecimal
print(0x11001001) #285216769

