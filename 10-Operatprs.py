#                                Operators

# Arithmetic, Realtional, Logical, Unary Operators.

#                     Arithmetic

x = 2
y = 4

print("Add :", x+y) #Add : 6
print("Sub :", x-y) #Sub : -2
print("Mul :", x*y) #Mul : 8
print("Div :", x/y) #Div : 0.5

x += 2
print(x) #4

x *= 4
print(x) #16

#                    Assignment 

# we can give multiple variables in one line.
a,b = 2,6
print(a) #2
print(b) #6


#                      Unary '-'

n=7
print(-n) #-7

n = -n
print(n) #-7

#                     Relational

i = 4
j = 5
k = 4
print(i >= j) #False
print(i >= k) #True
print(i == j) #false
print(i == k) #True
print(i != j) #True


#                      Logical
i1 = 4
j1 = 6
print(i1 > 5 and j1 >= 6) #False
print(i1 > 5 or j1 >= 6) #True

i2 = True
print(not i2) #False

i2 = not i2
print(i2) #False






