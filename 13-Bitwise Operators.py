#                               Bitwise Operator

# Complement'~', AND'&', OR'|', XOR'^', 
#left shift '<<', Right shift '>>'.

#                      Complement '~'

print(~13) #-14

#                      AND'&'

print(13 & 16) #0
print(14 & 18) #2
print(17 & 18) #16

#                      OR'|'

print(14 | 15) #15
print(16 | 13) #29

#                      XOR'^'

print(12 ^ 3) #15
print(16 ^ 13) #29
print(25 ^ 30) #7

#                      left shift '<<'

print(10 << 2) #40 (10 'gaining 2 bits' << 2)
print(13 << 3) #104

#                      right shift '>>'

print(10 >> 2) #2 (10 'lossing 2 bits' >> 2)
print(13 >> 3) #1 